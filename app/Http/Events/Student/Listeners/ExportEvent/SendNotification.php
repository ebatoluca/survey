<?php

namespace App\Http\Events\Student\Listeners\ExportEvent;

use App\Notifications\Student\ExportNotification;
use App\Http\Events\Student\Events\ExportEvent;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Queue\InteractsWithQueue;

class SendNotification
{

    public function __construct()
    {
        //
    }

    public function handle(ExportEvent $event)
    {

        $event->request->user()->notify((new ExportNotification($event->request))->locale($event->locale));

    }

}